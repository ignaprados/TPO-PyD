package main.Descuentos;

public class Descuento {
    private float descuento;

    //constructor
    public Descuento(float descuento) {
        this.descuento = descuento;
    }


    //getters
    public float getDescuento() {
        return descuento;
    }


    //setters
    public void setDescuento(float descuento) {
        this.descuento = descuento;
    }

}
